def addition(num1, num2)
  add = num1 + num2
  return add 
end

def subtraction(num1, num2)
  subs = num1 - num2
  return subs
end

def division(num1, num2)
  return num1/num2
end

def multiplication(num1, num2)
  return num1*num2
end

def modulo(num1, num2)
  return num1%num2
end

def square_root(num)
  Math.sqrt(num)
end
